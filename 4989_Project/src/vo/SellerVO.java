package vo;

public class SellerVO {
	private int seller_idx;	
	private String seller_id;
	private String seller_pwd;
	private String seller_phonenumber;
	private int seller_wallet;
	private String seller_joindate;

	public SellerVO() {}
	
	
	public int getSeller_idx() {
		return seller_idx;
	}



	public void setSeller_idx(int seller_idx) {
		this.seller_idx = seller_idx;
	}



	public String getSeller_id() {
		return seller_id;
	}



	public void setSeller_id(String seller_id) {
		this.seller_id = seller_id;
	}



	public String getSeller_pwd() {
		return seller_pwd;
	}



	public void setSeller_pwd(String seller_pwd) {
		this.seller_pwd = seller_pwd;
	}



	public String getSeller_phonenumber() {
		return seller_phonenumber;
	}



	public void setSeller_phonenumber(String seller_phonenumber) {
		this.seller_phonenumber = seller_phonenumber;
	}



	public int getSeller_wallet() {
		return seller_wallet;
	}



	public void setSeller_wallet(int seller_wallet) {
		this.seller_wallet = seller_wallet;
	}



	public String getSeller_joindate() {
		return seller_joindate;
	}



	public void setSeller_joindate(String seller_joindate) {
		this.seller_joindate = seller_joindate;
	}

	
}
